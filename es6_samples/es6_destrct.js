// Create an object
var myObj = {
  key1: "key-1",
  key2: "key-2"
};

// Unpack the object
var {key1, key2} = myObj;
console.log("key1: " + key1);
console.log("key2: " + key2);

// Create a list
var list = ["A", "B", "C"];

// Unpack the list
var [element1, element2] = list;
console.log("element1: " + element1);
console.log("element2: " + element2);
